import { earclip } from '../src';

import { expect, test } from 'bun:test';

test('empty', () => {
  expect(earclip([])).toEqual({
    vertices: [],
    indices: [],
  });
});

test('simple', () => {
  expect(
    earclip([
      [
        [0, 0, 0],
        [1, 0, 0],
        [0, 1, 0],
      ],
    ]),
  ).toEqual({
    vertices: [0, 0, 0, 1, 0, 0, 0, 1, 0],
    indices: [1, 2, 0],
  });
});

